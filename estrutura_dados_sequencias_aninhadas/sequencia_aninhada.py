print("Elemento 0 da Lista: ",lista[0])
print("Elemento 1 da Lista: ",lista[1])
print("Elemento 2 da Lista: ",lista[2])
print("Elemento 3 da Lista: ",lista[3])
print("Elemento 4 da Lista: ",lista[4])

# Indices Tupla
print("Elemento 0 da Tupla: ",tupla[0])
print("Elemento 1 da Tupla: ",tupla[1])
print("Elemento 2 da Tupla: ",tupla[2])
print("Elemento 3 da Tupla: ",tupla[3])
